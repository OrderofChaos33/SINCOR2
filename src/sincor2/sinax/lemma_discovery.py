"""
SINAX — Phase 6: Lemma Discovery Engine
==========================================
Identifies repeated failure regions in the proof graph, clusters them, and
generates candidate bridging lemmas.  Every candidate is routed through the
active verifier before being stored or surfaced.

Workflow
--------
1. **Detect bottlenecks** — gather nodes/edges with high failure density
   (via ``CurvatureAnalyzer``).
2. **Cluster failures** — group failed-edge source nodes by embedding
   similarity (greedy single-linkage in embedding space).
3. **Generate candidate lemmas** — propose a bridging lemma for each
   non-trivial cluster (using the cluster centroid and most common goal
   fragments).
4. **Rank and verify** — score candidates by potential impact; route
   each through the verifier; only return verified lemmas.

Public API
----------
    LemmaCandidate      — value object
    LemmaDiscoveryEngine
    get_lemma_engine()  — singleton accessor
"""

